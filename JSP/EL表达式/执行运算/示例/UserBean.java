package ServletDemoTest;
public class UserBean
{
    private String name;
    private String sex;
    public UserBean()
    {

    }
    public void setName(String name)
    {
        this.name = name;
    }
    public void setSex(String sex)
    {
        this.sex = sex;
    }
    public String getName()
    {
        return name;
    }
    public String getSex()
    {
        return sex;
    }
}
